// Operator +

function addition(n1, n2) {
  return n1 + n2;
}

// Operator -

function subtraction(n1, n2) {
  return n1 - n2;
}

// Operator *

function multiplication(n1, n2) {
  return n1 * n2;
}

// Operator /

function division(n1, n2) {
  return n1 / n2;
}

// Operator %

function remainder(n1, n2) {
  return n1 % n2;
}

// Operator **

function power(n1, n2) {
  return n1 ** n2;
}

